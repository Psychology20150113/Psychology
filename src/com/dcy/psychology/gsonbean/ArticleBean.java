package com.dcy.psychology.gsonbean;

public class ArticleBean {
	private int ArticleID;
	private String ArticleName;
	private String ArticleSmallImgUrl;
	private String ArticleDate;
	private int PublisherID;
	private String PublisherName;
	private String ArticleSummary;
	private String ArticleContent;
	private String ArticleImgUrl;
	
	public int getArticleID() {
		return ArticleID;
	}
	public void setArticleID(int articleID) {
		ArticleID = articleID;
	}
	public String getArticleName() {
		return ArticleName;
	}
	public void setArticleName(String articleName) {
		ArticleName = articleName;
	}
	public String getArticleSmallImgUrl() {
		return ArticleSmallImgUrl;
	}
	public void setArticleSmallImgUrl(String articleSmallImgUrl) {
		ArticleSmallImgUrl = articleSmallImgUrl;
	}
	public String getArticleDate() {
		return ArticleDate;
	}
	public void setArticleDate(String articleDate) {
		ArticleDate = articleDate;
	}
	public int getPublisherID() {
		return PublisherID;
	}
	public void setPublisherID(int publisherID) {
		PublisherID = publisherID;
	}
	public String getPublisherName() {
		return PublisherName;
	}
	public void setPublisherName(String publisherName) {
		PublisherName = publisherName;
	}
	public String getArticleSummary() {
		return ArticleSummary;
	}
	public void setArticleSummary(String articleSummary) {
		ArticleSummary = articleSummary;
	}
	public String getArticleContent() {
		return ArticleContent;
	}
	public void setArticleContent(String articleContent) {
		ArticleContent = articleContent;
	}
	public String getArticleImgUrl() {
		return ArticleImgUrl;
	}
	public void setArticleImgUrl(String articleImgUrl) {
		ArticleImgUrl = articleImgUrl;
	}
}
