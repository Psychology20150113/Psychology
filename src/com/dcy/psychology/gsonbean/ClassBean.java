package com.dcy.psychology.gsonbean;

public class ClassBean {
	private int ClassID;
	private String ClassTitleName;
	private String SubTitleName;
	private String ClassSummary;
	private String ClassSmallImgUrl;
	private String ClassDate;
	public int getClassID() {
		return ClassID;
	}
	public void setClassID(int classID) {
		ClassID = classID;
	}
	public String getClassTitleName() {
		return ClassTitleName;
	}
	public void setClassTitleName(String classTitleName) {
		ClassTitleName = classTitleName;
	}
	public String getSubTitleName() {
		return SubTitleName;
	}
	public void setSubTitleName(String subTitleName) {
		SubTitleName = subTitleName;
	}
	public String getClassSummary() {
		return ClassSummary;
	}
	public void setClassSummary(String classSummary) {
		ClassSummary = classSummary;
	}
	public String getClassSmallImgUrl() {
		return ClassSmallImgUrl;
	}
	public void setClassSmallImgUrl(String classSmallImgUrl) {
		ClassSmallImgUrl = classSmallImgUrl;
	}
	public String getClassDate() {
		return ClassDate;
	}
	public void setClassDate(String classDate) {
		ClassDate = classDate;
	}
}
